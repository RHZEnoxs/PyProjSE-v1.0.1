#Python 3 - GUI Programming (Tkinter)
# Python provides various options for developing graphical user interfaces (GUIs).
#  The most important features are listed below.
#
# Tkinter − Tkinter is the Python interface to the Tk GUI toolkit shipped with Python.
#  We would look this option in this chapter.
#
# wxPython − This is an open-source Python interface for wxWidgets GUI toolkit.
#  You can find a complete tutorial on WxPython here.
#
# PyQt −This is also a Python interface for a popular cross-platform Qt GUI library.
#  TutorialsPoint has a very good tutorial on PyQt here.
#
# JPython − JPython is a Python port for Java,
# which gives Python scripts seamless access to the Java class libraries on the local machine
# http://www.jython.org.